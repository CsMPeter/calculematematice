package org.fasttrackit;

public class Calcul {



    public int adunare(int x, int y){

        return x+y;
    }

    public int inmultire(int x, int y){

        return x*y;
    }

    public int despartire(int x, int y){

        return x/y;
    }

    public int scadere(int x, int y){

        return x-y;
    }
}
